"""Benchmark harness: measure token counts of HAAL vs. common formats.

Usage:
    python benchmarks/run.py            # writes results.json and RESULTS.md

Everything is measured, nothing is estimated: datasets are seeded and
deterministic, tokenizers are the public tiktoken vocabularies, and every
HAAL rendering is round-trip-verified against the source data before it
is counted.
"""

from __future__ import annotations

import json
import statistics
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).parent))

import yaml  # type: ignore[import-untyped]
from datasets import DATASETS

import haaland
from haaland.tokens import count_tokens

ENCODINGS = ("o200k_base", "cl100k_base")

OUT_DIR = Path(__file__).parent


def render_all(data: dict) -> dict[str, str]:
    """Serialize *data* in every compared format."""
    renderings = {
        "json": json.dumps(data, ensure_ascii=False, separators=(",", ":")),
        "json_pretty": json.dumps(data, ensure_ascii=False, indent=2),
        "yaml": yaml.safe_dump(data, sort_keys=False, default_flow_style=False, width=10**9),
        "haal": haaland.dumps(data),
        "haal_dense": haaland.dumps(data, profile="dense"),
    }
    # Correctness gate: never benchmark a rendering that doesn't round-trip.
    assert haaland.loads(renderings["haal"]) == data
    assert haaland.loads(renderings["haal_dense"], profile="dense") == data
    assert json.loads(renderings["json"]) == data
    assert yaml.safe_load(renderings["yaml"]) == data
    return renderings


def measure() -> dict:
    results: dict = {"datasets": {}, "ablations": {}}

    for name, factory in DATASETS.items():
        data = factory()
        renderings = render_all(data)
        entry: dict = {"bytes": {k: len(v.encode("utf-8")) for k, v in renderings.items()}}
        for encoding in ENCODINGS:
            counts = {k: count_tokens(v, encoding) for k, v in renderings.items()}
            base = counts["json"]
            entry[encoding] = {
                k: {"tokens": n, "vs_json_pct": round(100 * (base - n) / base, 1)}
                for k, n in counts.items()
            }
        results["datasets"][name] = entry

    # Ablation 1: delimiter choice (measured on the table-heavy datasets).
    delim_totals: dict[str, int] = {}
    for delim_name, delim in [
        ("comma", ","),
        ("tab", "\t"),
        ("pipe", "|"),
        ("semicolon", ";"),
        ("space", " "),
    ]:
        total = 0
        for factory in DATASETS.values():
            data = factory()
            text = haaland.dumps(data, delimiter=delim)
            assert haaland.loads(text, delimiter=delim) == data
            total += count_tokens(text, "o200k_base")
        delim_totals[delim_name] = total
    results["ablations"]["delimiter_o200k_total_tokens"] = delim_totals

    # Ablation 2: indent width.
    indent_totals: dict[str, int] = {}
    for width in (1, 2, 4):
        total = 0
        for factory in DATASETS.values():
            data = factory()
            text = haaland.dumps(data, indent=width)
            assert haaland.loads(text) == data
            total += count_tokens(text, "o200k_base")
        indent_totals[f"indent_{width}"] = total
    results["ablations"]["indent_o200k_total_tokens"] = indent_totals

    return results


def summarize(results: dict) -> dict:
    """Cross-dataset savings summary per tokenizer."""
    summary: dict = {}
    for encoding in ENCODINGS:
        per_fmt: dict[str, list[float]] = {}
        totals: dict[str, int] = {}
        for entry in results["datasets"].values():
            for fmt, r in entry[encoding].items():
                per_fmt.setdefault(fmt, []).append(r["vs_json_pct"])
                totals[fmt] = totals.get(fmt, 0) + r["tokens"]
        base_total = totals["json"]
        summary[encoding] = {
            fmt: {
                "total_tokens": totals[fmt],
                "overall_vs_json_pct": round(100 * (base_total - totals[fmt]) / base_total, 1),
                "mean_vs_json_pct": round(statistics.mean(v), 1),
                "min_vs_json_pct": min(v),
                "max_vs_json_pct": max(v),
            }
            for fmt, v in per_fmt.items()
        }
    return summary


FMT_LABELS = {
    "json": "JSON (compact)",
    "json_pretty": "JSON (2-space)",
    "yaml": "YAML",
    "haal": "HAAL (standard)",
    "haal_dense": "HAAL (dense)",
}


def write_markdown(results: dict, summary: dict) -> str:
    lines: list[str] = []
    w = lines.append
    w("# Benchmark results")
    w("")
    w("Generated by `python benchmarks/run.py`. All numbers are measured with the")
    w("public tiktoken BPE vocabularies on deterministic, seeded datasets defined in")
    w("`benchmarks/datasets.py`. Every HAAL rendering is round-trip-verified against")
    w("the source data before being counted. Baseline for savings: **compact JSON**")
    w("(no whitespace), the strongest common baseline.")
    w("")

    w("## Summary (all six datasets combined)")
    w("")
    for encoding in ENCODINGS:
        w(f"### Tokenizer: `{encoding}`")
        w("")
        w("| Format | Total tokens | vs. compact JSON | Range across datasets |")
        w("|---|---:|---:|---:|")
        for fmt in ("json", "json_pretty", "yaml", "haal", "haal_dense"):
            s = summary[encoding][fmt]
            rng = f"{s['min_vs_json_pct']:+.1f}% … {s['max_vs_json_pct']:+.1f}%"
            vs = "baseline" if fmt == "json" else f"{s['overall_vs_json_pct']:+.1f}%"
            w(f"| {FMT_LABELS[fmt]} | {s['total_tokens']:,} | {vs} | {rng} |")
        w("")

    w("## Per-dataset results")
    w("")
    for name, entry in results["datasets"].items():
        w(f"### `{name}`")
        w("")
        w("| Format | Bytes | o200k_base tokens | vs. JSON | cl100k_base tokens | vs. JSON |")
        w("|---|---:|---:|---:|---:|---:|")
        for fmt in ("json", "json_pretty", "yaml", "haal", "haal_dense"):
            o = entry["o200k_base"][fmt]
            c = entry["cl100k_base"][fmt]
            vs_o = "baseline" if fmt == "json" else f"{o['vs_json_pct']:+.1f}%"
            vs_c = "baseline" if fmt == "json" else f"{c['vs_json_pct']:+.1f}%"
            w(
                f"| {FMT_LABELS[fmt]} | {entry['bytes'][fmt]:,} | {o['tokens']:,} | {vs_o} "
                f"| {c['tokens']:,} | {vs_c} |"
            )
        w("")

    w("## Ablations (why HAAL's defaults are what they are)")
    w("")
    w("Total `o200k_base` tokens across all six datasets:")
    w("")
    w("| Delimiter | Total tokens |")
    w("|---|---:|")
    for k, v in results["ablations"]["delimiter_o200k_total_tokens"].items():
        w(f"| `{k}` | {v:,} |")
    w("")
    w("| Indent width | Total tokens |")
    w("|---|---:|")
    for k, v in results["ablations"]["indent_o200k_total_tokens"].items():
        w(f"| {k.replace('indent_', '')} space(s) | {v:,} |")
    w("")
    w("## Reproducing")
    w("")
    w("```bash")
    w('pip install -e ".[dev]"')
    w("python benchmarks/run.py")
    w("```")
    w("")
    return "\n".join(lines)


def main() -> None:
    results = measure()
    summary = summarize(results)
    payload = {"summary": summary, **results}
    (OUT_DIR / "results.json").write_text(json.dumps(payload, indent=2) + "\n", encoding="utf-8")
    (OUT_DIR / "RESULTS.md").write_text(write_markdown(results, summary), encoding="utf-8")

    for encoding in ENCODINGS:
        s = summary[encoding]["haal"]
        print(
            f"{encoding}: HAAL {s['total_tokens']:,} tokens total, "
            f"{s['overall_vs_json_pct']:+.1f}% vs compact JSON "
            f"(range {s['min_vs_json_pct']:+.1f}% … {s['max_vs_json_pct']:+.1f}%)"
        )
    print("wrote benchmarks/results.json and benchmarks/RESULTS.md")


if __name__ == "__main__":
    main()
